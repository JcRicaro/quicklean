     */
    public function store(AddUserRequest $request, User $user)
    {
        $user = $user->fill($request->only([
            'email'
            'name'
            ]));

        $user->password = bcrypt($request->get('password'));
        $user->save();

        return response()->json(['data' => $user, 'message' => 'User created!']);
    }

